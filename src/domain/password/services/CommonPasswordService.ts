export class CommonPasswordService {
  private readonly normalized = new Set<string>(
    COMMON_PASSWORDS.map((p) => this.normalize(p)),
  );

  normalize(input: string): string {
    return input.trim().toLowerCase();
  }

  isCommonPassword(password: string): { common: boolean; normalized: string } {
    const normalized = this.normalize(password);
    return { common: this.normalized.has(normalized), normalized };
  }
}

